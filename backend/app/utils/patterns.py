# ── Dynamic pattern access ────────────────────────────────────────────────
#
# 정적 목록(위)은 기준값으로 유지되며,
# korean_slang_updater 캐시에서 크롤링된 신조어/유행어가 병합된다.
#
# 사용법:
#   from app.utils.patterns import get_all_negative_patterns
#   patterns = get_all_negative_patterns()  # 정적 + 동적 합집합


def get_all_negative_patterns() -> list[str]:
    """정적 부정 패턴 + 동적 크롤링 패턴의 합집합을 반환한다.

    나무위키·국립국어원에서 수집된 신조어(샤갈, 현타, 킹받아 등)가
    24시간 주기로 갱신된 캐시에서 자동으로 포함된다.

    캐시가 없으면 정적 목록만 반환하므로 항상 안전하게 호출 가능하다.
    """
    try:
        from app.utils.korean_slang_updater import get_cached_negative_patterns
        dynamic = get_cached_negative_patterns()
    except Exception:
        dynamic = []
    return list(set(NEGATIVE_EMOTION_INTERJECTIONS + dynamic))


def get_all_positive_patterns() -> list[str]:
    """정적 긍정 패턴 + 동적 크롤링 패턴의 합집합을 반환한다."""
